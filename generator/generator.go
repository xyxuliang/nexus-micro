// req: 请求体（将序列化为 JSON）
func (c *Client) call(ctx context.Context, method string, req interface{}) (interface{}, error) {
	// 序列化请求体
	body, err := json.Marshal(req)
	if err != nil {
		return nil, fmt.Errorf("client: marshal error: %%w", err)
	}

	// 构建 HTTP 请求
	httpReq, err := http.NewRequestWithContext(ctx, http.MethodPost, c.baseURL+method, bytes.NewReader(body))
	if err != nil {
		return nil, fmt.Errorf("client: create request error: %%w", err)
	}
	httpReq.Header.Set("Content-Type", "application/json")

	// 发送请求
	httpResp, err := c.httpClient.Do(httpReq)
	if err != nil {
		return nil, fmt.Errorf("client: request error: %%w", err)
	}
	defer httpResp.Body.Close()

	// 读取响应
	respBody, err := io.ReadAll(httpResp.Body)
	if err != nil {
		return nil, fmt.Errorf("client: read response error: %%w", err)
	}

	// 解析统一响应格式
	var resp response.Response
	if err := json.Unmarshal(respBody, &resp); err != nil {
		return nil, fmt.Errorf("client: unmarshal response error: %%w", err)
	}

	if resp.Code != 0 {
		return nil, fmt.Errorf("client: api error [%%d]: %%s", resp.Code, resp.Msg)
	}

	return resp.Data, nil
}

