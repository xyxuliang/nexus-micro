// generateDTO 生成数据传输对象（DTO）。
// 从 DSL type 定义生成 Go 结构体，每个类型一个文件。
func (g *Generator) generateDTO(dir string, svc *ast.Service, file *ast.File) {
	for _, t := range file.Types {
		name := toSnakeCase(t.Name)

		var fields strings.Builder
		for _, f := range t.Fields {
			tag := fmt.Sprintf(`json:"%s"`, toSnakeCase(f.Name))
			fields.WriteString(fmt.Sprintf("\t%s %s `%s`\n", f.Name, goTypeToGo(f.Type), tag))
		}

		content := fmt.Sprintf(`// Code generated by Nexus Micro. DO NOT EDIT.
package dto

// %s %s
type %s struct {
%s}
`, t.Name, t.Doc, t.Name, fields.String())

		os.WriteFile(filepath.Join(dir, name+".go"), []byte(content), 0644)
	}
}

