// Call 调用远程 API。
// ctx:  请求上下文（支持超时和取消）
// path: API 路径（如 "/api/v1/users"）
// req:  请求参数（将序列化为 JSON）
func (c *Client) Call(ctx context.Context, path string, req interface{}) (interface{}, error) {
	body, err := json.Marshal(req)
	if err != nil {
		return nil, fmt.Errorf("client: marshal error: %%w", err)
	}

	httpReq, err := http.NewRequestWithContext(ctx, http.MethodPost, c.baseURL+path, bytes.NewReader(body))
	if err != nil {
		return nil, fmt.Errorf("client: create request error: %%w", err)
	}
	httpReq.Header.Set("Content-Type", "application/json")

	resp, err := c.httpClient.Do(httpReq)
	if err != nil {
		return nil, fmt.Errorf("client: request error: %%w", err)
	}
	defer resp.Body.Close()

	respBody, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, fmt.Errorf("client: read response error: %%w", err)
	}

	var apiResp response.Response
	if err := json.Unmarshal(respBody, &apiResp); err != nil {
		return nil, fmt.Errorf("client: unmarshal response error: %%w", err)
	}

	if apiResp.Code != 0 {
		return nil, fmt.Errorf("client: api error [%%d]: %%s", apiResp.Code, apiResp.Msg)
	}

	return apiResp.Data, nil
}

