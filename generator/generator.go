// generateRoutes 生成路由注册文件。
// 将所有 handler 的路由集中注册到 HTTP mux 中。
// SSE 端点使用 sse.NewChannelHandler 注册，绕过 JSON 响应包装。
func (g *Generator) generateRoutes(dir string, svc *ast.Service, file *ast.File) {
	var registrations strings.Builder
	for _, handler := range svc.Handlers {
		if handler.IsSSE {
			// SSE 端点：直接注册 http.Handler，绕过 JSON 包装
			registrations.WriteString(fmt.Sprintf(`	// %s: %s (SSE 实时推送)
	mux.Handle("%s%s", %sSSEHandler(svcCtx))
`, handler.Doc, handler.Path, svc.Prefix, handler.Path, handler.Name))
		} else {
			registrations.WriteString(fmt.Sprintf(`	// %s: %s
	mux.HandleFunc("%s%s", %sHandlerWrapper(svcCtx))
`, handler.Doc, handler.Path, svc.Prefix, handler.Path, handler.Name))
		}
	}

	content := fmt.Sprintf(`// Code generated by Nexus Micro. DO NOT EDIT.
// 路由注册 — 将所有 handler 注册到 HTTP mux。
package handler

import (
	"net/http"

	"%s/service/%s/internal/svc"
)

// RegisterRoutes 将所有 handler 注册到 HTTP ServeMux。
// 在 main.go 中调用此函数完成路由注册。
func RegisterRoutes(mux *http.ServeMux, svcCtx *svc.ServiceContext) {
%s}
`, g.module, svc.Group, registrations.String())

	os.WriteFile(filepath.Join(dir, "routes.go"), []byte(content), 0644)
}

