// generateRouter 生成路由注册文件。
// 使用 Gin 路由引擎，每个 handler 独立注册。
func (g *Generator) generateRouter(dir string, svc *ast.Service, file *ast.File) {
	var registrations strings.Builder
	for _, handler := range svc.Handlers {
		registrations.WriteString(fmt.Sprintf(
			`	// %s: %s %s%s
	api.%s("%s", h.%s(svcCtx))
`,
			handler.Doc, handler.Method, svc.Prefix, handler.Path,
			strings.ToUpper(handler.Method), handler.Path, handler.Name))
	}

	content := fmt.Sprintf(`// Code generated by Nexus Micro. DO NOT EDIT.
// Package router 提供 HTTP 路由注册。
package router

import (
	"net/http"
	"time"

	"github.com/gin-gonic/gin"
	"%s/core/middleware"
	"%s/service/%s/internal/handler"
	"%s/service/%s/internal/svc"
)

// Register 注册所有路由到 Gin 引擎。
func Register(r *gin.Engine, svcCtx *svc.ServiceContext) {
	h := handler.NewHandler()

	// 健康检查
	r.GET("/health", func(c *gin.Context) {
		c.JSON(http.StatusOK, gin.H{
			"code":    0,
			"message": "ok",
			"data": gin.H{
				"service": svcCtx.Config.Server.Name,
				"status":  "healthy",
				"version": "%s",
				"time":    time.Now().Format(time.RFC3339),
			},
		})
	})

	// 业务路由
	api := r.Group("%s")
	{
%s	}
}
`, g.module, g.module, svc.Group, g.module, svc.Group,
		file.Info.Version, svc.Prefix, registrations.String())

	os.WriteFile(filepath.Join(dir, "router.go"), []byte(content), 0644)
}

// goTypeToGo 将 .api 类型转为 Go 类型。
func goTypeToGo(apiType string) string {
	if strings.HasPrefix(apiType, "[]") {
		return "[]" + goTypeToGo(apiType[2:])
	}
	switch apiType {
	case "string":
		return "string"
	case "int", "int32":
		return "int32"
	case "int64":
		return "int64"
	case "float", "float64":
		return "float64"
	case "bool":
		return "bool"
	default:
		return apiType
	}
}

