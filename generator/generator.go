//       defer close(ch)
//       ticker := time.NewTicker(5 * time.Second)
//       defer ticker.Stop()
//       for {
//           select {
//           case <-ticker.C:
//               ch <- sse.NewEvent().WithType("update").WithData(data).Build()
//           case <-l.ctx.Done():
//               return
//           }
//       }
//   }()
//   return ch
