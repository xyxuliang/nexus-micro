#!/bin/bash
# =============================================================================
# Nexus Micro etcd Demo — 构建脚本
# =============================================================================
#
# 功能说明:
#   编译 etcd-demo 下的全部 4 个服务，将二进制文件输出到 bin/ 目录。
#   包含 user-service、home-service、payment-service 和 gateway。
#
# 使用方法:
#   ./build.sh              # 编译全部服务
#
# 输出目录结构:
#   examples/etcd-demo/bin/
#     ├── user-service      # 用户服务 (端口 8081/8082)
#     ├── home-service      # 首页服务 (端口 8083)
#     ├── payment-service   # 支付服务 (端口 8084)
#     └── gateway           # API 网关   (端口 8888)
#
# 前置条件:
#   - Go 1.25+ 已安装
#   - 项目 go.mod 已配置完成 (module github.com/xyxuliang/nexus-micro)
#
# 依赖关系:
#   - 本脚本由 start.sh 在启动前自动调用（当二进制文件不存在时）
#   - 也可独立执行，用于仅编译不运行的场景
# =============================================================================

# -e: 任何命令失败立即退出
# -u: 使用未定义变量时退出
# -o pipefail: 管道中任何命令失败都视为失败
set -euo pipefail

# ---------------------------------------------------------------------------
# 路径定义
# ---------------------------------------------------------------------------
# SCRIPT_DIR: 脚本所在目录 (examples/etcd-demo/)
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

# NEXUS_DIR: 项目根目录 (nexus-micro/) — 必须在根目录编译以解析 go.mod
NEXUS_DIR="$(cd "$SCRIPT_DIR/../.." && pwd)"

# BIN_DIR: 二进制文件输出目录
BIN_DIR="$SCRIPT_DIR/bin"

# ---------------------------------------------------------------------------
# ANSI 颜色码 — 终端彩色输出
# ---------------------------------------------------------------------------
RED='\033[0;31m'     # 错误信息
GREEN='\033[0;32m'   # 成功信息
BLUE='\033[0;34m'    # 进度信息
NC='\033[0m'         # 重置颜色

# ---------------------------------------------------------------------------
# 日志函数 — 统一的彩色日志输出
# ---------------------------------------------------------------------------
info()  { echo -e "${BLUE}[build]${NC} $*"; }    # 普通进度
ok()    { echo -e "${GREEN}[build] ✓${NC} $*"; } # 成功
err()   { echo -e "${RED}[build] ✗${NC} $*"; }   # 失败

# ---------------------------------------------------------------------------
# 确保输出目录存在
# ---------------------------------------------------------------------------
mkdir -p "$BIN_DIR"

# =============================================================================
# build_svc — 编译单个服务
# =============================================================================
# 参数:
#   $1 — 服务名称 (如 "user-service")
#   $2 — Go package 路径 (如 "./examples/etcd-demo/user-service/")
#
# 编译规则:
#   - 输出二进制到 $BIN_DIR/$name
#   - 编译失败则整体退出 (set -e)
# =============================================================================
build_svc() {
    local name="$1"   # 服务名称，也是输出文件名
    local pkg="$2"    # Go package 路径

    info "building $name..."
    if go build -o "$BIN_DIR/$name" "$pkg"; then
        ok "$name → bin/$name"
    else
        err "$name build failed"
        exit 1
    fi
}

# ---------------------------------------------------------------------------
# 切换到项目根目录，依次编译全部服务
# ---------------------------------------------------------------------------
cd "$NEXUS_DIR"

# ============================================
# 编译顺序 (无依赖关系，可调整)
# ============================================
build_svc "user-service"    "./examples/etcd-demo/user-service/"
build_svc "home-service"    "./examples/etcd-demo/home-service/"
build_svc "payment-service" "./examples/etcd-demo/payment-service/"
build_svc "gateway"         "./examples/etcd-demo/gateway/"

# ---------------------------------------------------------------------------
# 输出编译结果概览
# ---------------------------------------------------------------------------
echo ""
info "build complete:"
ls -lh "$BIN_DIR/"
